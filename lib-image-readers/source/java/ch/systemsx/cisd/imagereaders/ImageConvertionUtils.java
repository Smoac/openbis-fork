package ch.systemsx.cisd.imagereaders;

import java.awt.image.BufferedImage;
import java.awt.image.ColorModel;
import java.awt.image.RenderedImage;
import java.awt.image.WritableRaster;
import java.util.Hashtable;

/**
 * An utility class that can be used to facilitate {@link IImageReader} implementations.
 * 
 * @author Kaloyan Enimanev
 */
public class ImageConvertionUtils
{
    /**
     * converts a {@link RenderedImage} to {@link BufferedImage}.
     */
    public static BufferedImage convertToBufferedImage(RenderedImage img)
    {
        if (img instanceof BufferedImage)
        {
            return (BufferedImage) img;
        }
        ColorModel cm = img.getColorModel();
        int width = img.getWidth();
        int height = img.getHeight();
        WritableRaster raster = cm.createCompatibleWritableRaster(width, height);
        boolean isAlphaPremultiplied = cm.isAlphaPremultiplied();
        Hashtable<String, Object> properties = new Hashtable<String, Object>();
        String[] keys = img.getPropertyNames();
        if (keys != null)
        {
            for (int i = 0; i < keys.length; i++)
            {
                properties.put(keys[i], img.getProperty(keys[i]));
            }
        }
        BufferedImage result = new BufferedImage(cm, raster, isAlphaPremultiplied, properties);
        img.copyData(raster);
        return result;
    }

}
