package ch.systemsx.cisd.yeastx.db.generic;

import net.lemnik.eodsql.AutoGeneratedKeys;
import net.lemnik.eodsql.ResultColumn;

/**
 * The DTO for an experiment in the data mart.
 *
 * @author Bernd Rinn
 */
public class DMExperimentDTO
{
    @AutoGeneratedKeys
    private long id;
    
    @ResultColumn("PERM_ID")
    private String permId;
    
    private String name;

    public DMExperimentDTO()
    {
        // Bean compatibility.
    }
    
    public DMExperimentDTO(String permId)
    {
        this.permId = permId;
    }
    
    public long getId()
    {
        return id;
    }

    public void setId(long id)
    {
        this.id = id;
    }

    public String getPermId()
    {
        return permId;
    }

    public void setPermId(String permId)
    {
        this.permId = permId;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}
