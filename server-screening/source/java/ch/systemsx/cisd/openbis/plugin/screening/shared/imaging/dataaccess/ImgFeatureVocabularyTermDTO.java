package ch.systemsx.cisd.openbis.plugin.screening.shared.imaging.dataaccess;

import net.lemnik.eodsql.AutoGeneratedKeys;
import net.lemnik.eodsql.ResultColumn;

/**
 * Stores one feature vocabulary term definition.
 * 
 * @author Tomasz Pylak
 */
public class ImgFeatureVocabularyTermDTO
{
    @AutoGeneratedKeys
    private long id;

    @ResultColumn("CODE")
    private String code;

    @ResultColumn("SEQUENCE_NUMBER")
    private int sequenceNumber;

    @ResultColumn("FD_ID")
    private long featureDefId;

    @ResultColumn("DS_ID")
    private long dataSetId;

    // All Data-Object classes must have a default constructor.
    @SuppressWarnings("unused")
    private ImgFeatureVocabularyTermDTO()
    {
    }

    public ImgFeatureVocabularyTermDTO(String code, int sequenceNumber)
    {
        this.code = code;
        this.sequenceNumber = sequenceNumber;
    }

    public ImgFeatureVocabularyTermDTO(String code, int sequenceNumber, long featureDefId)
    {
        this.code = code;
        this.sequenceNumber = sequenceNumber;
        this.featureDefId = featureDefId;
    }

    public long getId()
    {
        return id;
    }

    public void setId(long id)
    {
        this.id = id;
    }

    public String getCode()
    {
        return code;
    }

    public void setCode(String code)
    {
        this.code = code;
    }

    public int getSequenceNumber()
    {
        return sequenceNumber;
    }

    public void setSequenceNumber(int sequenceNumber)
    {
        this.sequenceNumber = sequenceNumber;
    }

    public long getFeatureDefId()
    {
        return featureDefId;
    }

    public void setFeatureDefId(long featureDefId)
    {
        this.featureDefId = featureDefId;
    }

    public long getDataSetId()
    {
        return dataSetId;
    }

    public void setDataSetId(long dataSetId)
    {
        this.dataSetId = dataSetId;
    }
}
