package ch.ethz.sis.openbis.generic.server.asapi.v3.translator.person.search;

import ch.ethz.sis.openbis.generic.asapi.v3.dto.common.search.ISearchCriteria;
import ch.ethz.sis.openbis.generic.asapi.v3.dto.person.search.RegistratorSearchCriteria;
import ch.ethz.sis.openbis.generic.server.asapi.v3.translator.common.search.IObjectAttributeProviderFactory;
import ch.ethz.sis.openbis.generic.server.asapi.v3.translator.common.search.SearchObjectKind;
import ch.systemsx.cisd.openbis.generic.server.dataaccess.IDAOFactory;
import ch.systemsx.cisd.openbis.generic.shared.basic.dto.DataSetAttributeSearchFieldKind;
import ch.systemsx.cisd.openbis.generic.shared.basic.dto.ExperimentAttributeSearchFieldKind;
import ch.systemsx.cisd.openbis.generic.shared.basic.dto.IAttributeSearchFieldKind;
import ch.systemsx.cisd.openbis.generic.shared.basic.dto.MaterialAttributeSearchFieldKind;
import ch.systemsx.cisd.openbis.generic.shared.basic.dto.SampleAttributeSearchFieldKind;

/**
 * @author pkupczyk
 */
public class RegistratorSearchCriteriaTranslator extends PersonSearchCriteriaTranslator
{

    public RegistratorSearchCriteriaTranslator(IDAOFactory daoFactory, IObjectAttributeProviderFactory entityAttributeProviderFactory)
    {
        super(daoFactory, entityAttributeProviderFactory);
    }

    @Override
    protected boolean doAccepts(ISearchCriteria criteria)
    {
        return criteria instanceof RegistratorSearchCriteria;
    }

    @Override
    protected IAttributeSearchFieldKind getUserIdFieldKind(SearchObjectKind objectKind)
    {
        switch (objectKind)
        {
            case EXPERIMENT:
                return ExperimentAttributeSearchFieldKind.REGISTRATOR_USER_ID;
            case SAMPLE:
                return SampleAttributeSearchFieldKind.REGISTRATOR_USER_ID;
            case DATA_SET:
                return DataSetAttributeSearchFieldKind.REGISTRATOR_USER_ID;
            case MATERIAL:
                return MaterialAttributeSearchFieldKind.REGISTRATOR_USER_ID;
            default:
                throw new IllegalArgumentException("Unknown object kind: " + objectKind);
        }
    }

}
