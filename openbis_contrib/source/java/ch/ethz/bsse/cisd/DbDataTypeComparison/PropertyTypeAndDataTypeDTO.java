package ch.ethz.bsse.cisd.DbDataTypeComparison;

import net.lemnik.eodsql.AutoGeneratedKeys;
import net.lemnik.eodsql.ResultColumn;
import ch.systemsx.cisd.common.utilities.AbstractHashable;

/**
 * @author Manuel Kohler
 */

class PropertyTypeAndDataTypeDTO extends AbstractHashable
{
    @AutoGeneratedKeys
    private long id;

    @ResultColumn("ptCode")
    private String ptCode;

    @ResultColumn("dtCode")
    private String dtCode;

    PropertyTypeAndDataTypeDTO()
    {
        // All Data-Object classes must have a default constructor.
    }

    public int compareTo(PropertyTypeAndDataTypeDTO P)
    {
        return ptCode.compareTo(P.getPtCode());
    }

    public PropertyTypeAndDataTypeDTO(String ptCode, String dtCode)
    {
        this.ptCode = ptCode;
        this.dtCode = dtCode;
    }

    public long getId()
    {
        return id;
    }

    public void setId(long id)
    {
        this.id = id;
    }

    public String getPtCode()
    {
        return ptCode;
    }

    public void setPtCode(String ptCode)
    {
        this.ptCode = ptCode;
    }

    public String getDtCode()
    {
        return dtCode;
    }

    public void setDtCode(String dtCode)
    {
        this.dtCode = dtCode;
    }

}